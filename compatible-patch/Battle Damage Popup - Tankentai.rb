class Sprite_PopupLuna < Sprite
  
  #--------------------------------------------------------------------------
  # update_move
  #--------------------------------------------------------------------------
  def update_move
    self.x += @move_x
    self.y += @move_y
    self.x = (self.x - $sv_camera.x) * $sv_camera.zoom
    self.y = (self.y - $sv_camera.y) * $sv_camera.zoom
    @move_y += @gravity
  end
  
end # Sprite_PopupLuna